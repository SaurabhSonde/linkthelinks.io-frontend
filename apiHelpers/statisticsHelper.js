const Api = "http://localhost:4000/api";

export const getUserById = (userId, token) => {
  console.log(userId);
  return fetch(`${Api}/user/${userId}`, {
    method: "GET",
    headers: {
      Accept: "application/json",
      Authorization: `Bearer ${token}`,
    },
  })
    .then((response) => {
      return response.json();
    })
    .catch((err) => console.log(err));
};

